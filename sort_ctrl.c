#include "../push_swap.h"

int	sort_ctrl(t_data *p_s)
{
	int	x;

	x = p_s->last_a;
	while (x >= 1)
	{
		if (p_s->seta[x] < p_s->seta[x - 1])
		{
			x--;
		}
		else
			return (0);
	}
	return (1);
}
